package Java_LP1.Aula15do02;

public class Calculadora {
  
    public float soma(float n ,float n2){
        return n +n2;
    }
    public float subtra(float n ,float n2){
        return n -n2;
    }
    public float mult(float n ,float n2){
        return n *n2;
    }
    public float div(float n ,float n2){
        return n / n2;
    }
}


